def decode_patches():
    return {
        p: base64.b64decode("".join(v)).decode("utf-8")
        for p, v in PATCHES_B64.items()
    }

def check_node():
    ok_flag = True
    for cmd in ["node", "npm"]:
        if shutil.which(cmd):
            v = subprocess.run([cmd, "--version"], capture_output=True, text=True)
            ok(f"{cmd} -> {v.stdout.strip()}")
        else:
            err(f"{cmd} NON trovato. Installa Node.js >= 18: https://nodejs.org")
            ok_flag = False
    return ok_flag

def write_patches(project, patches):
    for rel, content in patches.items():
        dest = project / rel
        dest.parent.mkdir(parents=True, exist_ok=True)
        dest.write_text(content, encoding="utf-8")
        ok(rel)

def fix_tsconfig(project):
    ts = project / "tsconfig.json"
    if not ts.exists(): return
    c = ts.read_text()
    changed = False
    if "\"ES2020\"" in c and "ES2021" not in c:
        c = c.replace("\"ES2020\"", "\"ES2021\"")
        changed = True
    if "\"exclude\"" not in c:
        c = c.rstrip().rstrip("}") + ',\n  "exclude": ["src/tests"]\n}\n'
        changed = True
    elif "\"src/tests\"" not in c:
        c = c.replace("\"exclude\": [", "\"exclude\": [\"src/tests\", ")
        changed = True
    if changed:
        ts.write_text(c, encoding="utf-8")
        ok("tsconfig.json patchato: ES2021 + exclude src/tests")

def run_cmd(project, *cmd):
    print(f"  $ {chr(32).join(cmd)}")
    return subprocess.run(list(cmd), cwd=project).returncode == 0

